package com.switchcase;

public class SwitchCase {
	public static void main(String[] arg){
	int Name=10;
	switch(Name){
	case 10:
		System.out.println("Devi");
		break;
	case 20:
		System.out.println("Dhayali");
		break;
	case 30:
	    System.out.println("Dharani");
	    break;
   default:
	    	 System.out.println("default");
	}

}}
